# Calcular la aceleracion

# Declaracion de variables
aceleracion,velocidad_final,velocidaad_inicial,tiempo=0.0,0.0,0.0,0.0

# Asignacion de valores
velocidad_final= 30
velocidaad_inicial=10
tiempo=2

# Calculos
aceleracion=(velocidad_final-velocidaad_inicial/tiempo)

# Mostrar calculos
print ("aceleracion=", aceleracion)
print ("velocidad final=", velocidaad_final)
print ("velocidad inicial", velocidaad_inicial)
print ("tiempo=",tiempo)
